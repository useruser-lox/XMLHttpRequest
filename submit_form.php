<?php
// Встановлюємо заголовок, щоб клієнт знав, що ми повертаємо простий текст
header('Content-Type: text/plain');

// Перевіряємо, чи був запит методом POST і чи є дані
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    
    // Отримуємо та фільтруємо дані, надіслані через AJAX (наприклад, email та ім'я)
    $name = isset($_POST['name']) ? htmlspecialchars($_POST['name']) : 'Не вказано';
    $email = isset($_POST['email']) ? filter_var($_POST['email'], FILTER_SANITIZE_EMAIL) : 'Не вказано';
    
    // --- Тут може бути реальна логіка ---
    // Наприклад:
    // 1. Валідація даних
    // 2. Запис даних у базу даних (MySQL)
    // 3. Відправка email-повідомлення
    // ------------------------------------

    // Імітуємо успішну роботу
    if (empty($name) || empty($email)) {
         // Якщо дані пусті, повертаємо помилку (HTTP 400 Bad Request)
         http_response_code(400); 
         echo "Помилка: Необхідні поля (Ім'я та Email) не заповнені.";
    } else {
        // Повертаємо повідомлення про успіх (HTTP 200 OK - за замовчуванням)
        echo "Дані успішно отримано! Ваш запис: Ім'я: '{$name}', Email: '{$email}'.";
    }

} else {
    // Якщо до файлу звернулися не через POST-запит
    http_response_code(405); // Method Not Allowed
    echo "Помилка: Цей скрипт приймає тільки POST-запити.";
}
?>